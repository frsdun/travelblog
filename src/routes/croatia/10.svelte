    Dubrovnik is one of the most popular Adriatic tourist destinations, and
    everyday thousands of tourists disembark from cruise ships and are herded
    around the small Old Town in numbered groups by frazzled-looking tour
    guides. Hoping to avoid this madness, we made an early start to walk the
    city walls. The walk is about 2km long, and the walls 4-6m wide, and
    overlook sea and the red roofs and churches of the city. At each corner of
    the wall, there is a fortress which can be explored, and numerous turrets in
    between. We spent the rest of the morning escaping the crowds and the heat
    by exploring small side streets and alleys, and visiting some rather
    underwhelming museums.
  </p>
