  <p>
    Back in Split, we wandered across town to the Maritime museum, also housed
    in an old fortress. It housed an impressive collection of model ships
    throughout the ages, including the Yugoslav navy destroyer Split, which
    ironically attacked its namesake city in 1991. There was also an impressive
    display on the invention and subsequent use of torpedos.
  </p>
  <Gallery
    images={[{ src: 'img/croatia/croatia (95).jpg', alt: '' }, { src: 'img/croatia/croatia (89).jpg', alt: '' }]} />
  <p>
    We then managed to find Bacvice beach, Split’s “best” “sandy” beach. Beaches
    in Croatia are described as rocky, pebble, shingle or sandy, the latter
    being the most desirable. The water is warm and shallow and clear but the
    beaches themselves are average at best, with a small patch of dirty sand
    crowded with people roasting themselves in the sun. There are no waves and
    seemingly no tides, so they are a bit boring, and we are rather spoilt.
  </p>

  <p>
    We spent the evening climbing a small hill next to the city for beautiful
    sunset views, before descending for dinner in the city.
