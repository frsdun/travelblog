  <p>
    The old town of Split centres around the Diocletian Palace, built by the
    Roman Emperor in 295 AD, and transformed by subsequent generations ever
    since. Surrounded by high walls it is now a warren of apartments, shops,
    restaurants, museums, churches and squares; with a tangled maze of narrow
    alleyways. Under the palace, the basement was constructed to elevate the
    Emperor's apartments in order for him too have a better view and sea breeze.
    It perfectly mirrors the original floor plan of the palace above, and we
    explored the vast halls one evening after dinner.
  </p>
  <Gallery images={[{ src: 'img/croatia/croatia (92).jpg', alt: '' }]} />

  <p>
    We also took a bus to Klis fortress, built on a steep rock pinnacle next to
    a strategic mountain pass, originally fortified by the Romans. There is an
    impressive view across the valley towards Split and the Adriatic islands
    beyond. The fortress is remarkably intact, and makes for great exploring of
    cobblestone walkways, storehouse, barracks, lookout towers and a small
    chapel. A small museum explains how the Uskoks were fierce,undefeated
    warriors for more than 25 years before finally losing the fortress to the
    Ottomans.
  </p>
