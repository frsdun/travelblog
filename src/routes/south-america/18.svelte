  <p>
    We arrived in the driest non-polar desert in the world after our long
    overnight bus ride, tired, dusty and hot. San Pedro has been described as
    having 2000 residents, 1000 dogs and about 4000 tourists. This is
    immediately evident – there are dirty, dusty backpackers (much like
    ourselves) wandering the unpaved streets night and day. Single story adobe
    buildings house hostels, touristy craft shops and markets, and about a
    hundred tour operators, all offering the same 6 or so tours.
  </p>
  <p>
    After an early first night, we woke at the crack of dawn, and set off on our
    hired bicycles with our ridiculous watermelon helmets.
  </p>
  <p>
    We cycled about 15km to the Valle de la Luna, or the Moon Valley – aptly
    named for the barren lunar landscapes of sand crusted with salt, stripy rock
    cliffs, and rippled dunes.
  </p>
  <p>
    We arrived as the sun rose, and enjoyed the stark views and peace and quiet
    – there are daily tours here for sunset where the park fills up with
    hundreds of people but we only saw the first people arriving as we left,
    just in time to catch the heat of the day.
  </p>
  <p>
    We inadvertently picked up a very friendly dog on the way, who ran next to
    our bikes the whole morning, shared our water, and ended up following us all
    the way home (probably for the best rather than staying alone in the desert
    – stray dogs are generally well cared for in the towns and cities, and the
    locals regularly put out food, water and blankets on the pavements).
  </p>

