  <p>
    We flew back to warmer latitudes, and arrived in sweltering heat in
    Santiago. We were accommodated in a small apartment downtown with two guys
    (both named Juan) and their dog Molly, whose favourite pastime was to watch
    the world go by from the balcony, and was always ecstatic to see us.
  </p>
  <p>
    We set off on foot to explore the city, which we found to be more friendly
    and relaxed than Buenos Aires, but with a similar mix of European and
    American influence. We spent all of our mornings in excellent museums, the
    first of which was the Precolombian Art Museum, showcasing the history of
    indigenous South American people through art, ceramics and textiles before
    the arrival of European settlers. We saw some spectacular mummies from
    6000BC, unearthed in remarkable condition on account of the dry conditions
    in the Atacama desert. Also fascinating were these wooden carvings, which
    were placed over graves of the deceased of the Mapuche tribe.
  </p>
  <p>
    The second museum was the Museum of Memory and Human Rights, which documents
    the chilling human rights abuse, repression and censorship that occurred in
    the 70’s and 80’s under the dictator Pinochet. It is also housed in a
    fascinating building.x
  </p>
  <p>
    Another morning was spent exploring La Chascona, the home of Chile’s most
    famous writer, Pablo Neruda. He had 3 residences, which are all museums, and
    reflect his quirky taste and love of all things nautical. The house is
    terraced, below are photos from two of the gardens.
  </p>
  <p>
    In between all the museums, we also explored parks, plazas and pedestrian
    roads, which make for lively gathering points with buskers, chess players,
    mimes, vagrants, stray dogs, soap-box preachers and children swimming in the
    fountains – and are perfect spots for people-watching in the shade with an
    ice-cream!
  </p>

