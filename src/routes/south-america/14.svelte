  <p>
    Santiago lies in an enormous valley, surrounded by mountains with the Andes
    visible from most parts of the city, albeit through the smog. We embarked on
    a walk up Cerro San Cristobal, a hill in the middle of the city with a
    statue of Mary dedicated to the immaculate conception. From the summit, we
    had 360 degree views of the sprawling metropolis.
  </p>
  <p>
    We also had excellent views from Cerro Santa Lucia, another hill and a
    remnant of a 15-million year old volcano. It now consists of a city park,
    with beautiful gardens and fountains, steep stone stairways, a chapel, an
    old fort, and a lookout point with a plaque signed by Charles Darwin when he
    visited the area.
  </p>
  <p>
    A culinary highlight was our visit to Mercado Central (the central market)
    which comprises a large fish and seafood market, surrounding a central
    dining / restaurant area, the idea being that you can order fresh seafood
    off a menu of one of the restaurants, or ask them to cook your recent
    purchases from the market stalls. We amused ourselves at the sight of
    American tourists posing for photos with enormous king crabs that they had
    ordered (for R2250 a crab) while we enjoyed our delicious fish and chips and
    the bustling atmosphere.
  </p>
  <p>
    We have also discovered that Chileans seem to love rock music – we have been
    in a few taxis with demure-looking middle-aged drivers only to have Pink
    Floyd, the White Stripes or Led Zepplin blasting from the speakers
    (admittedly, interspersed with some Adele). On one of our evening walks we
    came across a rock band called “Traffic and Rock”, belting out guitar solos
    from Pink FLoyd and The Eagles next to the metro station, surrounded by
    homeless men sitting and drinking on their trolleys and a reasonably large
    crowd of locals rocking along.
  </p>
  <p>Overall, a friendly, vibrant city that we thoroughly enjoyed exploring.</p>

