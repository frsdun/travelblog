  <p>
    El Chaltén was our next destination, and one of the most anticipated. We
    arrived to a tiny town in a valley on the banks of a raging glacier-melt
    river to surprisingly good weather. Renown for gale force winds, rain and
    icy temperatures even in summer, we were lucky to find calm sunshine, and
    set off on some short walks to various lookout points after checking into
    our hostel.
  </p>
  <p>
    On the basis of the good weather and stunning scenery, we decided to hire a
    tent and sleeping bags, and set off on a 3 day overnight hike the next day.
    Bright and early, we set off towards Laguna De Los Tres, a lake close to the
    base of Fitzroy peak.
  </p>
  <p>
    We arrived at our campsite around lunch time, and after a quick bite to eat,
    set off up the steep path to the turquoise lakes and views of the rocky
    peaks of the mountains beyond. After an hour or two of admiring the scenery,
    we headed back down to our campsite, for a quick supper and bed.
  </p>
  <p>
    The next morning, we left views of Fitzroy behind us and walked past more
    fresh-water lakes and through beautiful forest towards Cerro Torre.
  </p>
  <p>
    We pitched camp next to a river, and spent the afternoon walking around
    Laguna Torre, another turquoise lake with a couple of icebergs floating on
    it from the quickly melting glacier.
  </p>
  <p>
    The mountains are reportedly home to many interesting animals, including
    pumas, however we were waking busy trails that kept the wildlife far away.
    We did however spot this enormous bird walking through our campsite
    (unidentified by us so far), and the woodpecker entertained us for a few
    minutes with its violent head-banging.
  </p>
  <p>
    The next day, we packed up camp and made it back to town by lunch, just in
    time to shelter from the howling wind which had begun as our walk ended.
    Tired, stiff and dusty, and after 3 days of spectacular weather and scenery,
    we were grateful for hot showers and a warm meal in the cosy hostel.
  </p>
