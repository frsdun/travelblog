  <p>
    We headed north up the coast from Valparaíso to the coastal town of La
    Serena, which seems to function largely as a holiday town for Chileans. The
    town has a small, historical center, and then a beachfront a short walk away
    lined with large apartment blocks. We spent the morning at the local museum,
    unremarkable except for an exhibit on Easter Island, including this enormous
    statue.
  </p>
  <p>
    We also wandered around the Japanese Gardens, before a quick lunch and then
    wandered along the beachfront in the afternoon. The weather was overcast and
    cool, the beach decidedly average with dirty brown sand and a lighthouse
    sinking slowly into said sand.
  </p>
  <p>
    We had a pleasant walk along the long promenade, followed by pizza for
    dinner overlooking the Pacific Ocean.
  </p>
  <p>
    The next day, we caught a quick bus ride inland to the tiny town of Vicuña,
    which lies in the Elqui Valley, where the famous national drink, Picso
    originates (unless you ask the Peruvians, who also claim it as their
    national drink. The competition is such that Chilean Pisco will be
    confiscated at the border with Peru). Lush vineyards lie in a valley in
    stark contrast to the surrounding barren, desert hills where only a few
    cactuses grow. We climbed to the top of the lookout point over town, upon
    which lies a small statue of Mary, dwarfed by towering radio masts.
  </p>
  <p>
    After a comfortable night in a B&B run by a friendly German lady, and a
    delicious breakfast, we set off on a tour of the large Pisco distillery.
    Farmers in the region harvest their Muscat grapes, and they are transported
    to the distillery run by the co-op CAPEL, where they are pressed, filtered,
    distilled and bottled. The end result is like a strong, sweet dessert wine,
    best served in Pisco Sours, with lime juice and sugar syrup.
  </p>
  <p>
    We then prepared ourselves for the journey back to La Serena, and the
    15-hour bus ride which was to follow.
  </p>

