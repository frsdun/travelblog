  title="Biking Bolivia"
  date={new Date(2019, 3 - 1, 21)}
  next="south-america/27"
  previous="south-america/25">

  <p>
    Inspired by the scenery and adrenaline of the Death Road the previous day,
    and with a recommendation from our guide, Duncan and David organised
    themselves a motorbike trip for our last day in La Paz.
  </p>
  <p>
    They set off with Juan their guide, a self-proclaimed “professional amigo”,
    who grabbed David’s GoPro and filmed large portions of their ride behind his
    back while riding one-handed.
  </p>
  <p>
    Their route took them from the Valley of Souls south of the city, down a dry
    river bed and through a small gold-mining village, before climbing steeply
    to an altitude of 5000m, with Illimani, a dormant 6400m volcano towering
    overhead.
  </p>
  <p>
    The narrow gravel road deteriorated with altitude, culminating in a
    river-crossing where they had to re-build a bridge that had been washed
    downstream.
  </p>
  <p>
    Meanwhile Alice and I set off to Tiwanaku, a UNESCO archeological site an
    hours drive out of town. We narrowly avoided been ripped off by a taxi
    driver, before arriving at what looked like a small abandoned farming town
    with some rocks lying about. We bought tickets from a run down stall, and
    hired an English-speaking guide to show us around the ruins.
  </p>
  <p>
    A series of stepped platforms have been partially uncovered in some places,
    and partially restored in others. The whole site is fascinating, an is
    presumed to have been an important Pre-Columbian spiritual and astronomical
    area. Large monoliths and arched gateways are decorated with sun and moon
    gods, and figures of llamas, pumas and condors feature prominently in the
    carvings. Two enormous concrete museums house a few ceramic and other
    artifacts found at the site. Although the whole area is rich in history and
    mystery, and undoubtedly an important Bolivian site, it feels completely
    underwhelming and desolate, with the potential to be a really interesting
    and informative museum(there was virtually no information available except
    through guides).
  </p>
  <p>
    Duncan and David returned late afternoon, via cable car (unfortunately for
    the locals who were trapped in their pod), delighted with themselves and in
    desperate need of food and a shower. The four of us then rode the cable car
    to a viewpoint to admire the city lights by night, before sitting down to a
    luxurious dinner of home-cooked roast chicken!
  </p>

