  <p>
    Our remaining time in South America was spent in Cusco, where we had a few
    days to explore the city.
  </p>
  <p>
    We visited a museum with artifacts found at Machu Picchu, and Qorikancha,
    initially a sacred Inca temple with walls covered in gold, which was ravaged
    by the Spanish, and now shares a site with the Church of Santo Domingo. We
    browsed the local markets, dodging crazy drivers in the narrow cobbled
    streets, and witnessed the “Lord of the Earthquakes”: every year on the
    Monday before Easter, a large statue of Jesus on the cross is adorned with
    garlands of red flowers and strings of red lights and paraded around town.
    In the 1600’s, locals believe that a painting of Jesus on the cross saved
    Cusco from a devastating earthquake, and this annual celebration pays homage
    to this event in a strange mix of Christianity and Andean tradition.
  </p>
  <p>
    Another day was spent exploring Inca ruins just outside the city, including
    the fort of Sacsaywaman, which lies on top of a hill overlooking Cusco, and
    has 3 massive parallel walls which zigzag for about 600m, showcasing amazing
    stonework and design. We also saw Pukapukara, “the Red Fort” due the pinkish
    colour of the rock used in construction, and Tambomachay, “The Bath of the
    Inca” where water is channelled down a series of tiers to form cold showers,
    where ceremonial baths are thought to have been conducted by Inca nobility.
  </p>
  <p>
    We then caught a quick flight to Lima, where we had one night before flying
    home. We spent the afternoon walking the streets of Miraflores, a modern
    upmarket surburb along the coast, where we watched surfers in the sea as the
    sun set, before enjoying a delicious seafood supper for our last evening.
  </p>
  <p>
    We have had an absolutely fantastic trip with perfect weather and great
    company along the way. Although there is so much more to see and do, we are
    glad to be coming home to friends and family, thank you to everyone for the
    messages and comments. Our next adventure begins in Cape Town…
  </p>

