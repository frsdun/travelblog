  <p>
    We left Puerto Natales by bus, heading further South to Punta Arenas, our
    final destination in Patagonia. We arrived to freezing cold weather with
    howling winds coming off the Magellan Straights. Walking around town in our
    down jackets and rain gear was by far the coldest we had been even after our
    night in a tent in the snow. The wind gets so bad that the town has ropes
    strung up between lamp posts for people to hold onto.
  </p>
  <p>
    Once we were used to the biting wind, we began to enjoy exploring the
    streets, and set off to the local cemetery. It consists of an elevated city
    block with similar mausoleums to those we had seen in Buenos Aires, with the
    exception of the precision-sculpted cypress trees lining the walkways.
  </p>
  <p>
    We spent an afternoon on the promenade, amused at the car washing service
    set up by the fire-brigade and the many locals eating ice-creams and wearing
    t-shirts, in the approximately 12 degree weather.
  </p>
  <p>
    The remaining days of our stay were occupied with various museum visits,
    including stately homes of prominent residences built around the boom of the
    town in the 1890’s, and the Naval Museum. But the most interesting was a
    private museum which builds replicas of famous ships. We were able to
    explore the replica of the Nao Victoria, Magellan’s ship that first
    circumnavigated the globe. There is also a replica of the Beagle (under
    construction), the James Caird (the lifeboat Shackelton sailed to South
    Georgia in) and the Ancud, a ship that claimed the Straights of Magellan for
    the newly independent Chile.
  </p>
  <p>
    We ended our stay in Punta Arenas with a delicious restaurant dinner of
    steak and king crab, before flying to Santiago.
  </p>

