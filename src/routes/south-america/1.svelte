  <p>
    After a 30 hour day chasing the sun, we finally arrived in downtown Buenos
    Aires to a quirky little yellow house. We have spent the first few days
    orientating ourselves and combating jet lag by walking the city.
  </p>
  <p>
    Breakfast on our first day was round the corner at an old coffee shop – in
    our limited Spanish we somehow managed to order 3 glazed croissants, a cup
    of coffee, a bottle of still water, a tiny glass of sparkling water and a
    tiny piece of cake – mostly just by saying “sí”.
  </p>
  <p>
    We have subsequently explored various barrios (neighbourhoods) including The
    Centre, home to the banking district, large shopping malls and
    pedestrianised roads, and Plaza De Mayo, the public square where porteño’s
    have famously protested pretty much everything throughout history. A welcome
    relief from the street noise and heat was a quick visit into the grand
    Catedral Metropolitana, home to the tomb of General José de San Martin,
    Argenitina’s most revered hero.
  </p>
  <p>
    Another morning was spent in Recoleta, one of the most wealthy barrios and
    home to BA’s famous cemetery. Here the rich and famous are buried in
    ornately decorated mausoleums, built from 1822 in various styles. Many have
    glass doors, revealing underground vaults stacked with coffins, urns,
    plaques and religious items left on marble altars.
  </p>
  <p>
    In a vain attempt at education, Duncan has also taken me on an architectural
    walking tour, of contemporary and modernist buildings, including a visit to
    this
    <strike>architectural masterpiece</strike>
    massive behemoth, the National Library.
  </p>
  <p>
    So far the “Paris of the South” has lived up to it’s name; with an array of
    proud citizens, beautiful women, historical buildings, crazy drivers,
    delicious food and graffiti…
  </p>

