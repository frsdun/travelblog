  <p>
    After a day of delay due to political protests in Bolivia, we boarded a bus
    to Cocacabana, on the shore of Lake Titicaca, the highest navigable lake in
    the world, and home to the Bolivian Navy (who keep alive the nations dreams
    of regaining access to the sea). Our bus ride involved a ferry trip across
    part of the lake, before arriving at Cocacabana, a small town that caters
    for religious pilgrims and tourists. We spent the afternoon walking along
    the lake shore with David and Alice, and a string of stray dogs in tow,
    before heading back to town to summit the small hill overlooking the town
    for sunset.
  </p>
  <p>
    The next day, we caught a boat across the choppy lake to Isla del Sol, a
    sacred Inca site, and currently home to about 800 families involved in
    farming, fishing and tourism. In between the small coastal villages, a
    barren, rocky ridge runs the length of the island, and we said goodbye to
    David and Alice, who were spending the night on the island, and set off on a
    hot hike along the ridge.
  </p>
  <p>
    We arrived on the other side of the island in time to catch the boat back to
    the mainland, before leaving on a night bus across the border to Peru and on
    to Cusco.
  </p>

