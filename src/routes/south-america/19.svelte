  <p>
    Turns out San Pedro is an industrious little dust bowl. We are feeling
    slightly disillusioned and ripped off in this tourist trap.
  </p>
  <p>
    On the day we arrived, we were overwhelmed by people offering tours and
    trying to charge us for various things at every turn. Apart from our cycle
    to Valle de la Luna, there is not a lot to see in the vicinity of town, and
    while the location is ideally situated for trips to the surrounding amazing
    natural scenery, all the tours involve fairly long bus rides.
  </p>
  <p>
    We had mostly anticipated this, and had planned and budgeted an itinerary
    before hand. When we arrived, we soon realised that all the tour operators
    had the same “official” prices, although they would offer discounts,
    especially if you booked multiple tours with them. We booked three tours
    with an agency: a trip to some nearby geysers, an astronomy evening, and our
    final 3-day tour into Bolivia.
  </p>
  <p>
    The tour to the geysers is a half day tour, about a 2-hour bus trip away,
    and the optimal time to arrive is just before dawn. As such, all the tours
    offer pick-ups from the hostels around town between 4:30 and 5am. We waited
    outside our hostel from 4:30 patiently, as everyone around us slowly
    departed on various buses. Duncan joked that it felt like he was the last
    kid to be picked for the sports team. By 5am, we were getting worried and
    phoned the agency, who assured us the bus was on the way. At 5:30am, we
    phoned them back, and were told there was a delay, but we would be picked up
    in the next 10-15 minutes. Thereafter, the phone number went to voicemail,
    and at 6:30am, we went back to bed.
  </p>
  <p>
    At 10am when the town came to life, we joined a queue of irate customers who
    had all been abandoned with no explanation. The charismatic tour operator
    apologised profusely, explaining that the bus driver had overslept and this
    was a “once a year event”. He offered us our money back and the tour for 2
    days time. This didn’t suit us – we were planning on leaving the next day.
    We eventually compromised on moving our itinerary forward by one day, and
    got a tour to some lakes instead for “free”. We then rebooked geyesers for
    the next day with a different company, and booked an extra night of
    accommodation. All of this contributed to a highly frustrating day. Having
    said that, our trip has really gone smoothly so far, and as setbacks go,
    certainly not the end of the world.
  </p>
  <p>
    In the afternoon, we set off on our consolation tour to a series of lakes,
    which turned out to be spectacular.
  </p>
  <p>
    The first lake, Lake Cejar, has a salt content of 30%, and similar to the
    Dead Sea, allows you to float. We had a refreshing although rather strange
    swim. The lakes are small, surrounded by a crust of salt, and then sand,
    with the snow-topped peaks of volcanoes in the distance. The salt crust is
    populated by small birds, and the lakes support life in spite of their high
    salt content, in the form of tiny shrimp which form the diet of the
    flamingos.
  </p>
  <p>
    We had an amazing afternoon and headed back to town, considerably cheered.
  </p>

