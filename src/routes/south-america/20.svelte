  <p>
    We woke up early the next day to catch our bus to the El Tatio geyser field,
    the third largest in the world, and one of the highest at 4300m. We arrived
    just before dawn at -3 degrees, the optimal time to appreciate the steamy
    landscape.
  </p>
  <p>
    Subterranean rivers flow through the valley, and are heated to boiling point
    (about 84 degrees at that altitude) by nearby pockets of volcanic magma. The
    boiling water is then forced to the surface through holes and cracks in the
    ground, where it bubbles and sometimes shoots into the cold morning air.
  </p>
  <p>
    We had breakfast as the sun rose in between exploring the 80-odd geysers,
    and then a quick swim in a hot spring. The water was actually luke warm at
    best, with hot patches near the inlet and occasionally coming up from
    below.x
  </p>
  <p>
    The rest of the morning was spent taking the scenic route back to town,
    where our guide pointed out local flora and fauna at various stops
  </p>
  <p>
    We also made a quick stop at the town of Machuca – a historic small
    settlement that now seems to just cater as a food and bathroom break for
    tourists. Lastly, our driver demonstrated the effects of a magnetite hill,
    as our bus coasted up the hill in neutral, before returning to town for a
    much needed siesta.
  </p>
  <p>
    At 11pm, we set out to learn a bit of astronomy. Northern Chile has some of
    the clearest skies in the world, and the locals pride themselves on the
    international and local research that is conducted in the area. We met up
    with a local amateur astronomer, who pointed out various constellations and
    gave us some insight into the mythology of the ancient Greek and Andean
    populations. We were lucky enough to have no moon, and marveled at Jupiter
    and four of it’s moons, Mars, and the nebula in Orion’s belt, among others
    through a large telescope, concluding our stay in San Pedro with a real
    highlight of the trip.
  </p>

