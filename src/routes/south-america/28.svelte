  <p>
    There are three ways to get to Machu Picchu, two of which (the Inca Trail
    and the train ride) require planning and booking in advance, which we had
    not done. We arranged our trip through an agent in Cusco at the last minute,
    and set of the next day on a harrowing 7-hour bus trip, up and down mountain
    switchbacks, our driver cutting corners at every opportunity on the winding
    and eroded road. The road ends at a hydro-electric power station next to the
    Urubamba river in a steep valley, and from here it was a 2 hour walk along
    the train line to Aguas Calientes, the small town at the base of Machu
    Picchu, where we spent the night.
  </p>
  <p>
    At 4.30am the next morning, we set off on a humid hike up the mountain
    through cloud forest to the entrance to Machu Picchu, and arrived to thick
    mist and hordes of people. We had a guide for the first 2 hours, who gave us
    a brief history, and (we think) made a lot of it up as he went. It is
    presumed to have been built as a retreat for the Inca emperor in the 1400â€™s,
    and was abandoned after about 100 years when the Spanish arrived in South
    America.
  </p>
  <p>
    Thereafter we were free to explore, and set off in the direction of the Inca
    Bridge, a narrow bridge along a cliff face which was initially used as an
    entrance to Machu Picchu, with amazing views into the valley below.
  </p>
  <p>
    By this time the mist was lifting, and we had spectacular views over the
    site. We spent the morning admiring the stonework, which varies in quality
    according to the purpose of the building. There is also a series of complex
    stone aqueducts, which channel water down to the many terraces, which have
    been built up in layers of stones, sand and topsoil to allow for adequate
    drainage.
  </p>
  <p>
    After too short a time exploring the fascinating ruins, we left to walk back
    to the hydro-electric station, where our bus was waiting to take us back to
    Cusco.
  </p>

