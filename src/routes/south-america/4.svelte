  <p>
    Above, some general street scenes. People in the city have some interesting
    idiosyncrasies:
  </p>
  <ul>
    <li>
      Everybody seems to have a dog, even though they live in tiny apartments
      (in the city at least). If you are too lazy or too busy, you can hire the
      services of a dog walker – these people walk around with up to 20 dogs on
      leashes at a time and make quite a sight.
    </li>
    <li>
      Platform shoes are in fashion – we don’t know why but they are hazardous
      on the uneven sidewalks
    </li>
    <li>
      Everybody talks on their cellphones like walkie-talkies (Mom, maybe you
      were ahead of your time)
    </li>
    <li>
      Nobody goes out for dinner before 10pm. Restaurants only open at 8pm, we
      arrived at 9pm, and at 10pm families with their kids started arriving.
      Similarly, shops open late in the mornings, and the locals don’t really
      seem to eat breakfast except to have a quick cup of coffee and a
      croissant.
    </li>
    <li>
      Very few people speak English, even in restaurants and museums. We try to
      get by and our Spanish is improving but there is always a question in
      every interaction that we don’t understand; it is a small victory every
      time to order a meal or pay for groceries without some kind of
      misunderstanding!
    </li>
  </ul>

