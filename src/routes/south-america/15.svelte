  <p>
    Valparaíso is a port city about 120km from Santiago, comprising of El Plan
    (the flat bit) and the cerros (the hills). Known as the “Jewel of the
    Pacific”, it was a major port for ships rounding the Americas before the
    construction of the Panama canal, although it still functions as one of
    Chile’s major commercial hubs.
  </p>
  <p>
    The city is a ramshackle patchwork of coloured houses, all of which look
    like they are about to slide off the 40 odd hills that overlook the bay. The
    streets are steep and narrow, and lined with graffiti – a combination of
    spectacular street art and spray-painted tags. In many places there is no
    road access, but a series of funiculars serve as public transport to the
    tops of the hills; the alternative is a long walk up steep staircases.
  </p>
  <p>
    We visited another house belonging Pablo Neruda, La Sebastiana, situated on
    top of one of the hills with 5 stories of semicircular rooms and fantastic
    views of the city. Once again the house was nautically themed, with low
    ceilings, creaking wooden floors and decorated with quirky objects and art
    from his travels.
  </p>
  <p>
    We also spent an afternoon visiting Viña del Mar, a nearby beach resort,
    lined with high-rise apartment blocks, expensive tourist restaurants, and
    none of the character of Valparaíso. We spent just long enough to eat our
    ice creams along the promenade, and pity the locals for their
    not-as-nice-as-Knysna beaches, which had dirty flat sea washing onto a thin
    strip of sand.
  </p>
  <p>
    The rest of our time was spent exploring the hills – around every corner
    there were beautiful views, rows of coloured houses, interesting street art,
    bohemian hippies, stray dogs and a building or two that looked like
    spontaneous collapse was imminent.
  </p>

