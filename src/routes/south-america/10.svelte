  <h2>Day 2: Camp Italiano to Camp Chileano</h2>
  <p>
    We woke up to a wet and muddy campsite, although fortunately we stayed dry
    through the night of rain. We set off in the still intermittent rain on an
    undulating path along another large lake. We plodded along for about 6
    hours, and then began the ascent to the next camp. We arrived exhausted
    after a long day, just in time to once again pitch camp in the rain. All the
    good campsites had been taken, and we had a spot with the end of our tent
    just about hanging off an eroded slope – this had no bearing on our quality
    of sleep, which was excellent! We also decided that the second night was a
    good time to take stock of all the food we had, and make sure we actually
    had enough for the trip – up until this point we had only vaguely planned
    our meals.
  </p>
  <h2>Day 3: Camp Chileano to Camp Serron</h2>
  <p>
    We were up early for our longest day, and started with the highlight of the
    park – the three towers. Although the trees were still dripping, the skies
    cleared as we made our steep ascent up the gorge and then up a large scree
    slope, before coming up over a ridge to a small lake and a most fantastic
    view.
  </p>
  <p>
    After some photos and a snack, we descended back to camp to pack up and
    continue with the rest of the day. By lunch, we were down in the valley at
    Hotel Torres, and by 3pm, only had 12km to go…
  </p>
  <p>
    We arrived after a 25km day at a very nice camp just after 7pm, once again,
    just as the rain started. We pitched our still damp tent, cooked a quick
    pasta dinner, and collapsed.
  </p>
  <h2>Day 4: Camp Serron to Camp Dickson</h2>
  <p>
    Our first official day of the “O” trail was quieter and for the first time,
    the recommended daily distance. We had spent the first 3 days combining
    “recommended” days of the “W”, in order to do the hike in 7 days, with one
    day at the end to travel back to town.
  </p>
  <p>
    We started with a steep ascent, and came around a corner to gale force
    winds, knocking us off our feet every few steps. We staggered along trying
    to keep our balance for about half an hour, before the wind settled a bit.
    The day was sunny and hot otherwise, and the walking was gentle ups and
    downs for about 18km in total. We arrived in camp as the wind picked up
    again, to a plague of ravenous mosquitos that we could not escape except in
    our tent. We huddled inside and it wasn’t long before the rain started…
  </p>

