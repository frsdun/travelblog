  <p>
    We left town the next day at 5am, and drove to the largest salt flat in the
    world, Salar de Uyuni, covering over 10 000sqm. We arrived before sunset to
    an incredible flat white crust, partially covered by a thin layer of water,
    reflecting the golden sky as the sun came up.
  </p>
  <p>
    We then hopped back in the Land Cruiser and set off across the vast expanse
    of white to the “salt hotel”, a rather tired looking building built from
    salt bricks, and evidently recently used during the 2016 Dakar rally.
  </p>
  <p>
    We had breakfast and hot tea, before driving out to the middle of nowhere,
    where we had some fun with forced perspective photos with Dalai the llama.
  </p>
  <p>
    After a stop in town for some curio shopping and an early lunch, including
    rather delicious llama meat, we made our way to the “train cemetery” on the
    outskirts of Uyuni. These trains have been left to the elements, the rusty
    skeletons sinking into the sand make and interesting visit.
  </p>

