  <p>
    We left San Pedro and set off on a three-day tour into Bolivia. We met our
    travel companions David and Alice from the UK, and Christian and Colette, a
    French couple. After crossing the Chilean border in San Pedro, we drove to
    the Bolivian border post in the desert where we paid an exorbitant amount
    for our visas, and met Manuel, our driver for the duration of the trip.
  </p>
  <p>
    After a quick breakfast, the six of us piled into the Land Cruiser, and set
    off across the sand, with views of ice-capped volcanoes in the distance. We
    had a few stops to look at spectacular lakes, before arriving at a hot
    spring where we had a warm swim.
  </p>
  <p>
    Although the sun was beating down, the wind was freezing, and for most of
    the day we stayed warmly dressed. We reached the highest point on the tour
    fairly quickly at 4900m, where we briefly stopped to see a geyser spurting
    boiling water and rather sulphuric fumes, before heading on to our overnight
    stop. The so-called “refugio” was a rustic building with 6-bed dorms, a
    dining area, and a small kitchen where our guide whipped up a lunch of
    bangers and mash for us.
  </p>
  <p>
    In the afternoon, we visited a nearby lake, coloured pink in parts, and
    teeming with flamingos. We amused ourselves watching a group of llamas, and
    a small mouse that scurried off.
  </p>
  <p>
    We spent the evening piling on all our warm clothes, and trying to
    acclimatise – we were still at over 4000m, and became short of breath with
    minimal exertion.
  </p>
  <p>
    The next morning we left after breakfast, and spent the day driving to to
    Bolivian town of Uyuni, which was to be our overnight stop. We had multiple
    stops along the way at various lakes and rock formations, but majority of
    the day was spent driving through thick sand with virtually no landmarks,
    which Manuel took in his stride.
  </p>
  <p>
    We found Uyuni (once a bustling trading hub) to be rather underwhelming, and
    these days seems to function largely as a departure point for tourists.
    After a very strange supper of cold chips, covered with onion, sausage,
    sliced egg and tomato, we had an early night.
  </p>

