  <p>
    Sharing the small headland, Cape Cross is home to a seal colony that ranges
    from 200 000 - 250 000 in number. Most of the seals are females and their
    pups, and they alternate between fishing and frolicking in the waves and
    basking on the shore. They call to each other constantly, and the noises and
    smells are unremitting. We also saw a few wounded jackal limping around -
    they prey on the seal pups, but the female seals are fiercely defensive and
    the jackal often come off second best.
  </p>
  <Gallery
    images={[{ src: 'img/namibia/namibia-137.jpg', alt: '' }, { src: 'img/namibia/namibia-133.jpg', alt: '' }]} />
  <p>
    Next we drove inland and soon had a view of Klein Spitzkoppe in the
    distance. In stark contrast to the cold and misty weather along the coast,
    we were back in the hot, dry desert once again. We arrived and set up one of
    our best camps yet. We had ostriches wandering around, loeries in the trees,
    spectacular sunsets, and an inspiring view of the mountain we were hoping to
    summit the next day.
  </p>
