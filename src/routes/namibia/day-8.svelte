  <p>
    We continued up the coast to see the seal colony at Cape Cross. This
    desolate spot is where Diogo Cao, an early Portuguese explorer erected a
    cross in 1486, and the inscription at the current replica reads: “In the
    year 6685 after the creation of the world and 1485 after the birth of
    Christ, the brilliant, far-sighted King John II of Portugal ordered Diogo
    Cão, knight of his court, to discover this land and to erect this padrão
    here.”
  </p>
