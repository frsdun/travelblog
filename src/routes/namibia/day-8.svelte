  <p>
    After a quick breakfast, we packed up and drove up the coast through the
    sand and the fog. We made a brief stop to the shipwreck of the Zeila, a
    fishing trawler that was stranded in 2008, and another to see Hentiesbaai, a
    popular fishing and holiday spot. The landmark in Hentiesbaai is the
    gallows, erected by the first two permanent residents of the town as a
    warning to visitors to pick up their litter. The tourism website assures
    that this gesture is in line with “typical Afrikaans humour”.
  </p>
