  <p>
    We woke up and packed up camp after breakfast, with a curious ostrich
    looking on. Our next destination was the White Lady Lodge near the Brandberg
    mountains. This almost circular granite mountain range makes a striking
    picture from above, and is a significant region for the San tribes. Hikers
    need a permit and a guide to walk in the mountains, which we unfortunately
    didn’t have time for.
  </p>
  <p>
    Somewhere along the road, we developed our second tyre puncture, and made
    quick work of changing it for the spare in the baking heat. We arrived in
    the small town of Uis, and spent most of the morning trying to find a new
    tyre (the punctured one was beyond repair) with the assistance of the staff
    at the rest camp in town. Eventually a kind lady drove to her house to find
    an old tyre for us to use as a spare - it needed some patching - and then
    finally we were on the road again.
  </p>
  <p>
    White Lady Lodge is an oasis in the middle of the desert, and we were glad
    to set up camp in the shade of some big trees. Unfortunately we didn’t see
    any of the desert elephants that frequent the camp at certain times of the
    year, but we spent the afternoon watching a variety of birds including a
    very curious and comical Yellow-Billed Hornbill and a small ground squirrel.
    Our relaxing afternoon ended with a swim and an early dinner.
  </p>

