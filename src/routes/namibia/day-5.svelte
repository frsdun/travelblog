  <p>
    Always up for a summit, we decided to climb the adjacent dune “Big Daddy”.
    It was only about 9:30am but the sun was already baking and the sand was
    soft. Like the proverbial frog in the well, we slid back down again with
    every step up, making hot and tiring work. We finally got to the peak and
    enjoyed spectacular 360 degree views over the surrounding desert dunes. We
    then ran back down the dune, which was considerably quicker and more fun
    than the hike up!
  </p>
  <Gallery images={[{ src: 'img/namibia/namibia-90.jpg', alt: '' }]} />
  <p>
    We drove back to camp for a swim and waited out the heat of the day in the
    shade, and then spent the rest of the afternoon exploring Sesriem canyon.
    Almost invisible from ground level, the canyon has been carved into the
    ground by water flowing through sedimentary rock over millions of years, and
    has a small permanent rock pool at one end which offers respite to many of
    the animals in the area.
