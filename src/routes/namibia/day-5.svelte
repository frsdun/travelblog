    We hopped on, and after a short drive and a hot walk, we laid eyes on the
    spectacular Deadvlei. The white clay pan is starkly contrasted by dark dead
    trees, and the surrounding orange dunes. And people everywhere, all trying
    to get a photo without any other people in it.
