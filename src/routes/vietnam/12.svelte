  <p>
    We woke up early to an ethereal misty sunrise, and set off on our scooters
    to Trang An, where we piled into a boat for a trip down the river. We chose
    a route that went through nine caves, and also stopped at three temples that
    we could visit on the way, and our local guide skillfully rowed us along the
    waterways.
  </p>
  <p>
    This turned out to be one of the highlights of our whole trip. The morning
    light was beautiful on the surrounding towering mountains, the caves were
    spectacular, most of them hundreds of metres long and with stalactites so
    low we had to duck to avoid hitting our heads.
  </p>
  <p>
    We had ample time to stretch our legs and explore the sites of the temples
    at stops along the way, and kept marvelling at the scenery around each new
    bend in the river.
  </p>
  <p>
    After lunch we went on a scootering adventure, with the plan to explore the
    small roads off the beaten track. These turned out to be mostly small,
    single track paths in between bright green rice paddies, and after a few
    dead ends down some small valleys, we found a circular route that took us
    through a farming community, with the road even going through the corner of
    a house under construction.
  </p>
  <p>
    Eventually, with sore bums, we made our way to Hang Mua, a small, somewhat
    disappointing cave at the bottom of a limestone peak. However, a climb of
    about 500 stairs brings you to the top of the peak, where a giant concrete
    dragon statue and 360 degree views await. We had planned to spend sunset
    here admiring the scenery, and prepared with some snacks and beers which
    went down well after the steep ascent.
  </p>
  <p>
    The next morning we went to Tam Coc for a boat trip along the waterways of
    another valley, this time with three caves, rice paddies on either side of
    the river, and with a lady who rowed our boat using her feet to propel the
    oars.
  </p>
  <p>
    After the quick morning excursion, we caught a bus to our next destination:
    Cat Ba Island.
  </p>

