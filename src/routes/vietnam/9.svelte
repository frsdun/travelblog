  <p>
    We left Hoi An and set off back to Danang, where we hopped on a train for
    one of the most beautifully scenic train trips. South of Denang, the beach
    front is under construction, with huge hotel and lifestyle resorts being
    built almost all the way to Hoi An.
  </p>
  <p>
    The northern coast is more steep and rugged, and so has been left untouched
    (for now). The train line hugs the coast in the jungle, high above the
    beaches and winds it’s way along, offering spectacular views.
  </p>
  <p>
    Hue is a historical city, where the Nguyen Dynasty emperors ruled and lived
    from from 1802 to 1945 in the Imperial City, and this huge citadel is now
    the main visitors attraction. We arrived early, shortly after opening and
    set about exploring inside the city walls.
  </p>
  <p>
    Over time, various emperors built and renovated different sections of the
    citadel, and let others fall into disrepair. Heavy bombing by the French and
    Americans also left much of the citadel in ruins, including the Forbidden
    Purple City. Previously the emperors private residence, the only servants
    allowed into this compound were eunuchs who posed no threat to his
    concubines. Unfortunately it has been completely destroyed, and what’s left
    of the foundations are overgrown with weeds.
  </p>
  <p>
    Elsewhere in the citadel, we explored temples, the private residences of the
    Queen’s mother, the Royal Theatre and beautiful gardens; all in various
    states of disrepair and reconstruction, but offering good insight into the
    grand, isolated lives of those previously in power.
  </p>

