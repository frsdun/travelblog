  <p>
    Our overnight train trip was surprisingly comfortable, and we arrived early
    the next morning in Danang, where we immediately departed for Hoi An. The
    Old Town of Hoi An is a UNESCO World Heritage site, and was a busy trading
    port from the 15th to the 19th century when the adjacent river silted up and
    became unnavigable for large ships.
  </p>
  <p>
    There are multiple preserved traditional houses, temples, assembly areas and
    museums, with evidence of Chinese and Japanese architectural influence. In
    addition, many craftsman and tailors, shops, restaurants and street vendors
    all make for a lively atmosphere. There are also some great little alleys to
    explore, devoid of tourists and offering a glimse into the lives of the busy
    town’s residents.
  </p>
  <p>
    We visited the Hoi An museum, where we had an overview of the history of the
    town, including some strong propoganda displays regarding the role of
    residents in the war against the Americans and their “puppet army”. We also
    visited the Ceramics Museum, which housed ceramic items recovered from
    archeological sites near the town, as well as from a shipwreck off the
    coast. It was displayed in an old traditional house, mostly made of wood,
    with no windows but a beautiful central courtyard to provide light and
    ventilation.
  </p>
  <p>
    We also visited the House of Tan Ky, a renowned merchant whose descendants
    still live on the upper floor of the house today. The ground floor has much
    of the family’s original furniture, and has been well maintained and
    restored considering that the house floods with up to 1.5m of water every
    rainy season – the family has pulley systems rigged up to lift all the
    furniture in the event of a flood.
  </p>
  <p>
    But the real charm of the town comes alive after dark, when hundreds of
    strings of coloured lanterns strung across the narrow roads are lit up,
    creating a magical ambience.
  </p>
  <p>
    We had a delicious fine-dining dinner of traditional dishes unique to Hoi
    An: banh bao (rose-shaped dumplings with a shrimp centre), crispy spring
    rolls, cao lau (pork, greens and noodles), and the most delicious pineapple
    parfait with coconut ice-cream for dessert.
  </p>

