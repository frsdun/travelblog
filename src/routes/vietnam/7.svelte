  <p>
    We ended our stay in Da Lat with a quick scooter ride around town, and a
    visit to the Linh Phuoc Pagoda, and elaborate temple decorated with coloured
    mosaic tiles.
  </p>
  <p>
    We then left by bus for Nha Trang. Our buses so far have been of the
    “sleeper” variety , with everyone in almost fully reclining seats, feet
    tucked under the head end of the person in front of you. Duncan has
    struggled to fit his legs in the given space, but other than that they make
    for a fairly comfortable ride.
  </p>
  <p>
    Nha Trang is a coastal town that reminds us very much of Durban beach front:
    East facing, long stretches of sand and a nice promenade to walk along, only
    with bigger hotels and more Russian tourists.
  </p>
  <p>
    We had a day of exploring, and saw two excellent local photographers
    galleries, and visited a fascinating museum dedicated to Alexandre Yersin; a
    doctor, microbiologist, agriculturalist, astronomer and explorer among his
    many other talents. He is credited with having discovered the bacteria that
    caused the bubonic plague in the 1890’s, and lived and worked in Nha Trang
    for many years where he was reportedly loved and revered by the Vietnamese
    people.
  </p>
  <p>
    After a long walk on the beachfront and an ice-cream, we set off on an
    overnight train, heading north to Danang.
  </p>

