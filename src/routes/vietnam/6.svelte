  <p>
    We arrived in the town of Da Lat after a 7 hour bus ride, and were greeted
    by our host “Mr Happy” with hugs and dinner. After hearing that we were on
    honeymoon, he had upgraded our room and we arrived to decorative towel
    sculptures and rose petals strewn around the room.
  </p>
  <p>
    Da Lat was less hot and humid than the South had been, and more mountainous.
    Desiring to explore and escape the tourists, we rented a scooter and set off
    to find some of the waterfalls in the region.
  </p>
  <p>
    The Vietamese seem to have an insatiable love of all things kitsch, and as
    one guide book describes it, nature is the secondary attraction. Think music
    blaring from speakers along pathways, fake flowers tied into trees, hawkers
    selling loads of plastic junk, haphazardly erected huts, fake rock, lion
    statues, etc…
  </p>
  <p>
    There are a few very touristy waterfalls around Da Lat that have been
    “enhanced” as such, and then some off the beaten track that are relatively
    unspoilt. We drove about 50km out of town, initially along a beautiful
    mountain pass road through pine forests. Da Lat (according to the locals)
    has the best climate for growing fruit, vegetables and flowers, and the
    valleys are filled with greenhouses and rows of colourful flowers and crops.
  </p>
  <p>
    We arrived at Pongour waterfall, our first stop and one of the busier and
    more touristy spots. Duncan’s photos make it look like we were the only
    people there but we spent a few amusing minutes watching tourists slipping
    and sliding on rocky ledges while trying desperately to capture the perfect
    selfie.
  </p>
  <p>
    We got back on our scooter, which was making some funny noises that we chose
    to ignore. Our next stop was Bao Dai Waterfall; more spectacular, more
    remote and this time we really were the only people there. This site is much
    more tasteful, with some landscaped gardens near the entrance and some steps
    down the steep descent, but fortunately no hawkers and plastic flowers here.
  </p>
  <p>
    Our final stop was Elephant Waterfall, after a long meandering ride through
    some beautiful villages and farmlands. A very steep descent brought us to
    the bottom of these thundering falls, surrounded by moss covered boulders.
  </p>

