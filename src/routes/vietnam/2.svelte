  <p>
    Racing Boy is the name of Chris’s scooter, with whom we have quickly become
    familiar. 90% of vehicles on the road are scooters of some kind, and they
    move in herds and swarms together in a critical mass of buzzing engines and
    honking horns. Chris and Ana introduced us to their neighbourhood on our
    first day, and we set off on the back of their scooters to find breakfast
    and to see the sights.
  </p>
  <p>
    Our first stop was the Phu Loi Prison, which was used in WWII as well as the
    Indochina and Vietnamese wars to house political prisoners. It has been
    turned into a museum, with very lifelike models of prisoners and guards in
    the old detention cells.
  </p>
  <p>
    We then set off to see the cathedral and the largest reclining Budda in
    Vietnam, interspersed with some street food and fancy ice-cream, before
    heading home for a quick break.
  </p>
  <p>
    In the afternoon, we found some dirt roads along the nearby river where
    Duncan and I practiced our scootering skills, and we set off on a beautiful
    river-side drive. We saw a swallow farm, another pagoda and a funeral
    procession, followed by cafe sua da – strong Vietnamese coffee with
    condensed milk, poured over ice.
  </p>
  <p>
    Dinner consisted of delicious chicken, with sticky coconut rice and
    deep-fried pineapple at “The Fancy Chicken Place” (according to Chris),
    after which we took a stroll along the river which was illuminated by string
    of lights in the trees.
  </p>

