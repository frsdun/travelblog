  <p>
    The next morning, we joined a small tour group and hopped on a local boat
    for a cruise around the adjacent bay. We stopped at Monkey Island, the
    ubiquitous tour group hop-off point, where we climbed to a viewpoint and
    watched some monkeys playing in the bushes nearby.
  </p>
  <p>
    We then set off to a protected part of Lan Ha Bay, where we explored some
    hidden bays and caves by kayak. We narrowly avoided getting ourselves into a
    water fight with Chris and Ana, and focussed on paddling in a synchronised
    fashion in our kayak that had a tendency to turn right.
  </p>
  <p>
    We had a lunch on the deck of a floating house, complete with a small fish
    farm adjoining the deck. Many locals live in these floating villages with
    their pets and livestock, with some boats providing services such as
    supermarkets, petrol stations and schools.
  </p>
  <p>
    We then went to a cliff over the sea for some deep water soloing – rock
    climbing on a cliff face without ropes or any protection, the idea being
    that when you complete the climb or if you fall, you land in the water
    below. The boat drove up to the rock face, where we climbed onto the rock.
    Someone supported the climber while the boat reversed to a safe distance
    away, and then we climbed as far and as high as we wanted to, before jumping
    into the sea for a refreshing swim back to the boat. The obvious problem
    being, the higher you climb, the scarier the jump… Rather terrifying but
    exhilarating!
  </p>
  <p>
    We made our back the main land as the sun was setting, and then spent the
    night of our trip in Hanoi, where we saw a traditional Water Puppet Theatre
    show. The next morning we explored the Imperial Citadel of Hanoi, the first
    royal enclosure dating back to 1010, and expanded on by many subsequent
    dynasties; with artefacts of building materials and pottery found at the
    site dating back centuries. Most recently, building D67 housed the north
    Vietnamese military headquarters from 1954 – 1975, complete with underground
    tunnels and bunkers.
  </p>
  <p>
    After an interesting morning and one last Vietnamese coffee for Duncan (he
    has become something of an addict, although I suspect it has more to do with
    the condensed milk than the caffeine), we started our journey back home. We
    left Chris and Ana in Ho Chi Minh City before flying back to SA – thank you
    for being such great hosts and travel buddies. Thank you also to everyone
    who joined us and wished us well for our wedding, married life has started
    spectacularly and we can’t wait for the next adventure!
  </p>

