  <p>
    On our third day, we started off with a delicious but unusual meal of pork
    chops and rice at Chris’s favourite breakfast spot. We then set out to the
    Cu Chi Tunnels, an extensive network of underground tunnels of huge
    strategic importance, used by the Viet Cong during the American war. The
    tunnels are narrow, only 60cm in height, and extend hundreds of kilometres
    with multiple levels, and numerous dead ends and traps within the network.
  </p>
  <p>
    We had the opportunity to squeeze through one of the entrances, and saw
    areas used as bunkers, infirmaries for wounded soldiers, living spaces, and
    workshops where unexploded American bombs and ammunition were carefully
    disassembled to make other weapons. We were also shown underground dining
    and cooking areas, with a series of underground chambers designed to hold
    and disperse smoke from cooking in thin, hardly noticeable wisps at the
    surface that mingled in with the early morning mist.
  </p>
  <p>
    While the dense jungle has recovered to some extent following extensive
    bulldozing and use of defolients like Agent Orange, hundreds of large
    craters still scar the landscape from the bombs dropped by B52’s. A sobering
    tour, with some horrific truths and a small dose of propaganda from a
    different perspective to the Western rhetoric we’ve been exposed to.
  </p>
  <p>
    The next day, Ana planned an epic scooter adventure for us, and we set off
    in the morning to visit Black Virgin Mountain, the highest point in South
    Vietnam at just under 1000m. We started out in the tail-end of rush-hour
    traffic through town, navigating between locals on scooters transporting
    everything from two huge packets of hundreds of live goldfish, to a very
    large pane of glass.
  </p>
  <p>
    The scenery became more rural and the traffic less stressful as the ride
    went on, until we were on small farm roads between plantations of rubber
    trees and beautiful green fields.
  </p>
  <p>
    Black Mountain itself has multiple temples scattered on its slopes, reached
    by cable car and hundreds of stairs. We attempted a hike to the summit on a
    steep path of boulders, and unfortunately strewn with litter. Half-way up we
    ran out of time and had to content ourselves with views of patchwork fields
    across the valley below.
  </p>
  <p>
    Our return scooter journey of about two hours left us with sore bums and
    frazzled nerves – mine anyway, Duncan is exhilarated and already planning
    the next trip on two wheels!
  </p>

